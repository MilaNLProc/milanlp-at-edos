from dataclasses import dataclass, field
from typing import Any, Dict, List, Mapping, Optional, Union

import torch
from datasets import Dataset
from tqdm import tqdm
from transformers import (
    MODEL_FOR_MASKED_LM_MAPPING,
    DataCollatorForLanguageModeling,
    DataCollatorWithPadding,
)

MODEL_CONFIG_CLASSES = list(MODEL_FOR_MASKED_LM_MAPPING.keys())
MODEL_TYPES = tuple(conf.model_type for conf in MODEL_CONFIG_CLASSES)



@dataclass
class DataCollatorWithP(DataCollatorWithPadding):
    def __call__(self, features: List[Dict[str, Any]]) -> Dict[str, Any]:
        sizes = [len(e["input_ids"]) for e in features]
        max_length = max(sizes)  # we don't pad to multiple of ...

        batch_items = list()
        for e in features:
            label = e.pop("labels")
            item = {k: v + [0] * (max_length - len(v)) for k, v in e.items()}
            item["labels"] = label
            batch_items.append(item)

        keys = batch_items[0].keys()
        batch = dict()
        for key in keys:
            batch[key] = torch.LongTensor([e[key] for e in batch_items])

        if "label" in batch:
            batch["labels"] = batch["label"]
            del batch["label"]
        if "label_ids" in batch:
            batch["labels"] = batch["label_ids"]
            del batch["label_ids"]
        return batch


@dataclass
class FineTuningArguments:
    target: str = field()
    text_column_name: str = field(default="text")
    patience: int = field(default=3)
